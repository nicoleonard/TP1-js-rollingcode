const numero = parseInt(prompt("ingrese un numero para verificar su divisibilidad"));

if(numero%2==0){
    document.write("<p>El numero es divisible en 2</p>");
}
if(numero%3==0){
    document.write("<p>El numero es divisible en 3</p>");
}
if(numero%5==0){
    document.write("<p>El numero es divisible en 5</p>");
}
if(numero%7==0){
    document.write("<p>El numero es divisible en 7</p>");
}